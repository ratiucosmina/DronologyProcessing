package org.azd.enums;

public enum PullRequestStatus {
    /***
     * Pull request is abandoned.
     */
    ABANDONED,
    /***
     * Pull request is active.
     */
    ACTIVE,
    /***
     * Used in pull request search criteria to include all statuses.
     */
    ALL,
    /***
     * Pull request is completed.
     */
    COMPLETED
}
